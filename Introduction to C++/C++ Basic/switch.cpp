#include<bits/stdc++.h>
using namespace std;

int main() {
    int choice = 2;

    switch (choice)
	{
        case 1:
            cout << "Option 1 selected." << endl;
            break;
        case 2:
            cout << "Option 2 selected." << endl;
            break;
        case 3:
            cout << "Option 3 selected." << endl;
            break;
        default:
            cout << "Invalid option." << endl;
            break;
    }

    return 0;
}
